		<div><span style="text-decoration: underline; color: #006652;"><a href="https://polischuk-bestattungen.de/images/PDF/Checkliste.pdf" target="_blank" style="color: #006652; text-decoration: underline;">Eine Checkliste der notwendigen Dokumente</a></span></div>
<div>&nbsp;</div>
<div><span style="font-family: arial,helvetica,sans-serif;">Im Todesfall befinden sich die Hinterbliebenen oft in einer Ausnahmesituation, insbesondere wenn</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;">es keine Vorsorge gibt. Viele Fragen kommen auf, die Angehörigen sind unsicher, was die Wünsche</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;">des Verstorbenen und die nun zu erledigenden Formalitäten betrifft. Als Bestattungsdienstleister</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;">übernehmen wir bei Polischuk-Bestattungen alle Besorgungen bei Behörden und Konsulaten,</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;">wichtige Formalitäten und organisieren die Bestattungen mit Beisetzung in Deutschland</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;">oder die Überführungen weltweit.</span></div>
<div>&nbsp;</div>
<div><span style="font-family: arial,helvetica,sans-serif;">Wichtige Dokumente:</span></div>
<div>&nbsp;</div>
<div><span style="font-family: arial,helvetica,sans-serif;"><span style="color: #006652;">♦</span> Reisepass oder Personalausweis der verstorbenen Person</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;"><span style="color: #006652;">♦</span> bei ledigen oder minderjährigen Personen: standesamtliche Geburtsurkunde, bei ausländischen</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;">Bürgern wird eine Übersetzung verlangt</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;"><span style="color: #006652;">♦</span> bei Verheirateten: ebenfalls die Geburtsurkunde sowie das Stammbuch (Heiratsurkunde)</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;"><span style="color: #006652;">♦</span> bei Verwitweten: die zwei Urkunden wie bei Verheirateten und zusätzlich die Sterbeurkunde des</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;">verschiedenen Ehegatten</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;"><span style="color: #006652;">♦</span> bei Geschiedenen: Geburts- und Heiratsurkunde und entsprechendes, rechtskräftiges</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;">Scheidungsurteil</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;"><span style="color: #006652;">♦</span> Todesbescheinigung (Leichenschauschein)</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;"><span style="color: #006652;">♦</span> bei Überführungen: Angabe der Heimatadresse</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;"><span style="color: #006652;">♦</span> Krankenkassenkarte des Verstorbenen</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;"><span style="color: #006652;">♦</span> Rentennummer(n)</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;"><span style="color: #006652;">♦</span> diverse Versicherungspolicen mit den letzten Beitragsquittungen z.B. für eine Sterbegeld-,</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;"><span style="color: #006652;">♦</span> Lebens- oder Unfallversicherung</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;"><span style="color: #006652;">♦</span> Beitrags- oder Mitgliedsbescheinigungen von Verbänden, Vereinen oder ähnlichen</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;">Organisationen, die Beihilfen oder Sterbegelder gewähren</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;"><span style="color: #006652;">♦</span> Testament oder Hinterlegungsschein für das Amtsgericht bzw. den Notar</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;"><span style="color: #006652;">♦</span> Vertrag der Bestattungsvorsorge</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;"><span style="color: #006652;">♦</span> falls bereits vorhanden den Grabstellennachweis, auch bei einem Grab im Ausland</span></div>
<div>&nbsp;</div>
<div><span style="font-family: arial,helvetica,sans-serif;">Sollten Ihnen Urkunden oder Dokumente fehlen, sprechen Sie uns an und ein Mitarbeiter von</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;">Polischuk- Bestattungen hilft Ihnen gerne weiter. Wir wissen aus Erfahrung, bei welchen Ämtern die Papiere zu</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;">besorgen sind oder der Ersatz &nbsp;zu beantragen ist. Benötigen Sie Übersetzungen von Dokumenten,</span></div>
<div><span style="font-family: arial,helvetica,sans-serif;">können Sie sich ebenfalls an uns wenden.</span></div>	</div>

	
	
				
	
	
	
